        Tentative d'assassinat
    </h3>

    <article class="blog-post">
        <p style="font-size: 1.1rem;">Robert Nesta MarleyNote 1 est né le 6 février 1945Note 2 dans la ferme de ses grands-parents maternels,
            1976, c'est la sortie de Rastaman Vibration (1976) qui sera le disque de Bob Marley & The Wailers le plus vendu de son vivant et son premier succès américain. La sortie de l'album est suivi par une tournée américaine puis anglaise à la fois éreintante pour les musiciens car très longue et extraordinaire sur le plan musical[En quoi ?]. Le phénoménal concert donné au Roxy de Los Angeles le 26 mai 1976 est là pour témoigner de l'énergie et la puissance des Wailers sur scène à cette époque. Bob Marley est au sommet de sa puissance scénique durant cette Rastaman Vibration Tour.

            1976, c'est aussi une période très dure pour la Jamaïque, au bord de la faillite après la crise pétrolière de 1973. Le gangstérisme s'est considérablement développé sur l’île, qui n'a plus grand chose à voir avec la Jamaïque célébrant son indépendance en 1962. La violence, amplifiée par l'opposition virulente entre le JLP (Jamaica Labour Party) d'Edward Seaga et le PNP (People's National Party) de Michael Manley, élu depuis 1972, pousse le gouvernement à décréter l'état d'urgence le 19 juin 1976, à quelques mois des élections.

            Le 3 décembre 1976 à Kingston, peu avant le grand concert gratuit en plein air Smile Jamaica qui a lieu à la demande du premier ministre jamaïcain Michael Manley, Bob Marley (qui ne soutenait pas Michael Manley mais s'était engagé dans ce concert pour la Jamaïque et le peuple jamaïcain en souffrance, les « Sufferers ») échappe à une fusillade déclenchée à son domicile par six hommes armés, alors qu'il répétait avec son groupe dans l’Island House au 56 Hope RoadNote 3. Une balle lui érafle la poitrine et vient se loger dans son bras gauche26, tandis qu'une autre touche Rita à la tête, mais sans la tuer. Don Taylor, le manager américain de Bob Marley, en sort très gravement blessé de cinq balles. Des doutes persistent sur l'origine de cette tentative d'assassinat. Au-delà des déclarations ici et là impliquant le JLP, la CIA, voire des représailles en direction d'Allan « Skill » Cole qui aurait truqué des paris, on ne peut aujourd'hui dire avec certitude qui est vraiment à l'origine de cet attentat. Bob Marley, au cours d'une interview de 1977, sera assez évasif sur le sujet27 et évoquera cet épisode dramatique dans sa chanson Ambush in the Night sur l'album Survival publié en 1979.


            Bob Marley en concert, 1980, Zurich, Suisse.
            Deux jours après l'attentat qui a failli lui coûter la vie, Bob Marley participe comme prévu au concert Smile Jamaica à Kingston. Aux journalistes qui lui demandent pourquoi il tient tant à jouer lors de ce concert, il répond : « Les gens qui tentent de rendre ce monde mauvais ne prennent jamais de jours de congés. Comment le pourrais-je ? » Ashton « Family Man » Barrett, caché dans les collines, est remplacé ce jour-là par Cat Coore de Third World. À son arrivée sur scène, Bob Marley montre ses bandages au public, tel un martyre[réf. nécessaire]..</p>

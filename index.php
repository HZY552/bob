            <h3 class="pb-4 mb-4 fst-italic border-bottom pt-5">
                Héritage musical religieux
            </h3>

            <article class="blog-post">
                <p style="font-size: 1.1rem;">L’héritage culturel et musical de Bob Marley est aujourd’hui considérable. Bob, le chanteur, le rastaman, le rebelle, le prophète, dont les images s’affichent sur les murs du monde entier, est devenu une icône, au même titre que Che Guevara, Martin Luther King ou Marilyn Monroe. Aux quatre coins du monde, la figure de Bob Marley est présente, sur les murs des quartiers, dans les boutiques de souvenirs, dans les bacs des disquaires. Bob Marley reste partout le symbole de la lutte contre l’oppression et de l’appel à l’amour universel. Manu Chao a dit lors d'une interview qu’un tee-shirt de Bob Marley était mieux qu’un gilet pare-balles pour traverser les quartiers chauds de la planète ! C’est dire l’importance de Bob Marley aujourd’hui encore et partout dans le monde.

                    Bob Marley, en huit ans de carrière internationale, a donné une dimension exceptionnelle au reggae et a considérablement influencé la musique dans son ensemble, qu’il s’agisse du pop rock occidental, de la variété internationale et de ce qu’on appelle aujourd’hui la « World Music ». Même si d’autres figures majeures du reggae comme Burning Spear, Peter Tosh, Culture ou Toots & the Maytals ont contribué à la diffusion mondiale de cette musique, Bob Marley a été, entre 1973 et 1980 le vaisseau amiral du reggae à travers la planète. Et même après sa mort en 1981, les disques Confrontation (1983) et Legend (1984) étendent encore son influence. Ses morceaux ont été depuis 1981 joués, repris, remixés par un nombre considérables d’artistes et de DJ. De Eric Clapton à Ben Harper, en passant par Lauryn Hill et Tracy Chapman, les chansons de Bob Marley traversent tous les répertoires musicaux depuis plus de 30 ans.

                    Mais Bob Marley, c’est encore un message à la fois social et religieux. Il représente aujourd’hui encore l’image de la dénonciation de l’oppression des Noirs par les Blancs, mais aussi des pauvres par les riches, des analphabètes par les gens éduqués, etc. De Slave Driver, Crazy Baldhead, Get Up Stand Up, jusqu’à Revolution, Burnin' & Lootin', Rat Race ou War, sa voix porte encore le refus de la domination, de l’oppression, de la ségrégation (« We refuse to be, what you wanted us to be » ; Babylone System : Survival) et appelle à la lutte pour l’émancipation et l’égalité des droits humains ( « Stand Up for your rights… Don’t give up the fight » ; Get Up, Santd Up : Burnin’). Mais au-delà de la rébellion et de la lutte, Bob Marley n’a cessé d’appeler à l’amour universel et à la communion des hommes. Son « One love, One heart » (« Let's get together and feel all right ») n’est rien d’autre qu’un message de paix et d’amour. Tout comme Is This Love, No Woman No Cry, So Much Trouble in the World, etc.

                    Bob Marley, c’est encore un message religieux, celui de Jah Rastafari, incarné aux yeux des rastas par l’empereur d’Ethiopie Haïlé Sélassié Ier, vu comme le messie sur terre. Bob était profondément religieux, il faisait partie des Douze Tribus d'Israel (Twelve Tribes of Israel), lisait la Bible plusieurs fois par jour, allait à la messe le dimanche, commençait tous ses concerts à partir de 1978 par le fameux « Greetings in the Name of His Majesty Emperor Haile Selassie I ; Jah, Rastafari ! ». Nombreux sont ceux, de son vivant comme aujourd’hui, qui voient Bob Marley comme un prophète au service de Jah Rastarafi ou, voire comme la réincarnation du Christ (Tyrone Downie44). Le message rastafari est omniprésent dans les chansons de Bob Marley : Forever Loving Jah, Rastaman Chant, Jah Live, Rastaman Vibration, etc. Les concerts de Bob Marley sont, en plus d'être de puissantes manifestations scéniques et musicales, à voir comme de grands messes où se mêlent l’indignation, la rébellion et l’amour, mais aussi la célébration de Jah et l’appel au bonheur des humains à travers la pratique des préceptes rastafari.</p>
